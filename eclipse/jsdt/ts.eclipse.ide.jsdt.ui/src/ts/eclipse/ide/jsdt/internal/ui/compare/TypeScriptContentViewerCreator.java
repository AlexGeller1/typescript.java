package ts.eclipse.ide.jsdt.internal.ui.compare;

import org.eclipse.compare.CompareConfiguration;
import org.eclipse.compare.IViewerCreator;
import org.eclipse.jface.viewers.Viewer;
import org.eclipse.swt.SWT;
import org.eclipse.swt.widgets.Composite;

/**
 * Required when creating a TypeScript MergeViewer from the plugin.xml file.
 */
public class TypeScriptContentViewerCreator implements IViewerCreator {

	public Viewer createViewer(Composite parent, CompareConfiguration mp) {
		return new TypeScriptMergeViewer(parent, SWT.NULL, mp);
	}
}
